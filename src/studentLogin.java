    private void btn_stuloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_stuloginActionPerformed
        String stuem = txt_stuem.getText();
        String stupass = new String(txt_stupass.getPassword());       
        
        try {
            PreparedStatement pstLog = con.prepareStatement("SELECT * FROM studentinfo_tbl WHERE stu_Em = ?");
            pstLog.setString(1, stuem);
            rs = pstLog.executeQuery();
            
            if (rs.next()) {
            String em = rs.getString("stu_Em");
            String password = rs.getString("stu_Pass");

            if ((stuem.equals(em)) && stupass.equals(password)) {
                JOptionPane.showMessageDialog(null, "SUCCESS");

                // Insert the email into tealogin_tbl
                try {
                    PreparedStatement pstInsert = con.prepareStatement("INSERT INTO stulogin_tbl (stu_Em) VALUES (?)");
                    pstInsert.setString(1, stuem);
                    pstInsert.executeUpdate();
                } catch (SQLException ex) {
                    // Handle the exception, e.g., display an error message
                    ex.printStackTrace();
                }

                new studentMain().setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "INCORRECT!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "User not found!");
        }
            
        } catch (SQLException ex) {
            
        }
    }//GEN-LAST:event_btn_stuloginActionPerformed

