    private void playSound() {
    try {
        File soundFile = new File("C:\\Users\\Paul Justin\\Documents\\NetBeansProjects\\ITEC PROJ\\src\\Images\\bgmusc.wav");
        if (soundFile.exists()) {
            AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(soundFile);
            successClip = AudioSystem.getClip(); // Assign to the class-level variable
            successClip.open(audioInputStream);
            successClip.loop(Clip.LOOP_CONTINUOUSLY);
            successClip.start();
        } else {
            System.out.println("Sound file does not exist.");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
    
    
