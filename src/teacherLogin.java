    private void btn_tealoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tealoginActionPerformed
    String teaem = txt_teaem.getText();
    String teapass = new String(txt_teapass.getPassword());       

    try {
        PreparedStatement pstLog = con.prepareStatement("SELECT * FROM teainfo_tbl WHERE tea_Em = ?");
        pstLog.setString(1, teaem);
        rs = pstLog.executeQuery();

        if (rs.next()) {
            String em = rs.getString("tea_Em");
            String password = rs.getString("tea_Pass");

            if ((teaem.equals(em)) && teapass.equals(password)) {
                JOptionPane.showMessageDialog(null, "SUCCESS");

                // Insert the email into tealogin_tbl
                try {
                    PreparedStatement pstInsert = con.prepareStatement("INSERT INTO tealogin_tbl (tea_Em) VALUES (?)");
                    pstInsert.setString(1, teaem);
                    pstInsert.executeUpdate();
                } catch (SQLException ex) {
                    // Handle the exception, e.g., display an error message
                    ex.printStackTrace();
                }

                new teacherMain().setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "INCORRECT!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "User not found!");
        }
    } catch (SQLException ex) {
        // Handle the exception, e.g., display an error message
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btn_tealoginActionPerformed

