    private void btn_admloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_admloginActionPerformed
        String admem = txt_admaem.getText();
        String admpass = new String(txt_admpass.getText());

        try {
            pst = con.prepareStatement("SELECT * FROM adminfo_tbl");
            rs = pst.executeQuery();

            while(rs.next()) {

                String em = rs.getString("adm_em");
                String password = rs.getString("adm_pass");

                if((admem.equals(em)) && (admpass.equals(password)) ){

                    JOptionPane.showMessageDialog(null, "SUCCESS");
                    new adminMain().setVisible(true);
                    this.setVisible(false);

                }else {

                    JOptionPane.showMessageDialog(null, "INCORRECT!");

                }

            }

        } catch (SQLException ex) {

        }
    }//GEN-LAST:event_btn_admloginActionPerformed

